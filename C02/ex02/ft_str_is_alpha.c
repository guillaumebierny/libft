int	ft_str_is_alpha(char *str)
{
	int	i;

	i = -1;
	while (str[++i])
	{
		if ((!(str[i] >= 97 && str[i] <= 122))
			&& (!(str[i] >= 'A' && str[i] <= 'Z')))
			return (0);
	}
	return (1);
}
