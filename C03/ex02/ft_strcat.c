char	*ft_strcat(char *dest, char *src)
{
	int	i;
	int	t;

	i = -1;
	t = -1;
	while (dest[++i])
	{
	}
	while (src[++t])
	{
		dest[i++] = src[t];
	}
	dest[i] = '\0';
	return (dest);
}
