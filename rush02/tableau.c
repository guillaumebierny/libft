#include <stdio.h>

int main(int argc, char **argv)
{
	int i;
	int j;
	int t;

	char tab[13][3];

	i = ft_len(argv[1]);
	t = 0;
	j = 0;

	while (--i >= 0)
	{
		if (t == 3)
		{
			t = 0;
			j++;
		}
		tab[j][t] = argv[1][i];
		t++;
	}
}
