#include <unistd.h>

int	main(int argc, char **argv)
{
	int	i;
	int	t;

	i = 0;
	while (argv[++i])
	{
		t = -1;
		while (argv[i][++t])
			write(1, &argv[i][t], 1);
		write(1, "\n", 1);
	}
	(void)argc;
}
